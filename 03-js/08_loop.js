/**
 * n = 5 입력
 *      *
 *     **
 *     ***
 *    ****
 *    *****
 *
 * n = 3 입력
 *     *
 *     **
 *     ***
 */
// Hint 1) prompt 사용해서 높이를 입력 받기 - 10진수의 정수(int)형으로 형변환 할 것
// Hint 2) 각 줄의 내용을 저장할 문자열 변수 만들기
// Hint 3) 문자열과 문자열을 더한다
// Hint 4) for문 중첩

let result = '';
let number = parseInt(prompt('숫자 입력'), 10);

for (let i = 1; i <= number; i++) {
  for (let j = 1; j <= number - i; j++) {
    result += ' ';
  }
  for (let k = 1; k <= i; k++) {
    result += '*';
  }
  result += '\n';
}
console.log(result);
